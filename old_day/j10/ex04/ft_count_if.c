int		ft_count_if(char **tab, int (*f)(char*))
{
	int y;
	int nb;

	nb = 0;
	y = 0;
	while (tab[y])
	{
		if (f(tab[y]) == 1)
			nb++;
		y++;
	}
	return (nb);
}
