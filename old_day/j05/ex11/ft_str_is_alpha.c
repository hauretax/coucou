int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while ((str[i] <= 'z' && str[i] >= 'a' && str[i]) || (str[i] <= 'Z' &&
			str[i] >= 'A' && str[i]))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
