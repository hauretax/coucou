char		*ft_strrev(char *str)
{
	int		i;
	int		y;
	char	x;

	i = 0;
	y = 0;
	while (str[i])
		i++;
	i--;
	while (i > y)
	{
		x = str[i];
		str[i] = str[y];
		str[y] = x;
		y++;
		i--;
	}
	return (str);
}
