int		ft_atoi(char *str)
{
	int	t[3];

	t[1] = 0;
	t[0] = 0;
	t[2] = 1;
	while ((str[t[0]] <= 32 || (str[t[0]] >= '0' && str[t[0]] <= '9') ||
			str[t[0]] == '-' || str[t[0]] == '+') && (!(str[t[0]] == '-'
					&& str[t[0] + 1] == '-')))
	{
		if (str[t[0]] == '-' && t[2] != -1)
			t[2] = -1;
		while ((str[t[0]] >= '0' && str[t[0]] <= '9') || str[t[0]] == '+')
		{
			if (str[t[0]] == '+')
				t[0]++;
			if (str[t[0]] >= '0' && str[t[0]] <= '9')
				t[1] = t[1] + str[t[0]++] - '0';
			if (str[t[0]] >= '0' && str[t[0]] <= '9')
				t[1] = t[1] * 10;
			if (!(str[t[0]] >= '0' && str[t[0]] <= '9'))
				return (t[1] * t[2]);
		}
		t[0]++;
	}
	return (0);
}
